// Track used responses to avoid short-term repetition
let usedEncouragementIndices = [];
let usedSuggestionIndices = [];
const MAX_HISTORY = 50; // Remember last 50 used responses

// Get a random mood response that hasn't been used recently
function getMoodResponse() {
    // Get unused encouragement
    let availableEncouragements = MOOD_RESPONSES.encouragements
        .map((_, i) => i)
        .filter(i => !usedEncouragementIndices.includes(i));

    if (availableEncouragements.length === 0) {
        // Reset if all used
        usedEncouragementIndices = [];
        availableEncouragements = MOOD_RESPONSES.encouragements.map((_, i) => i);
    }

    const encIndex = availableEncouragements[Math.floor(Math.random() * availableEncouragements.length)];
    usedEncouragementIndices.push(encIndex);
    if (usedEncouragementIndices.length > MAX_HISTORY) {
        usedEncouragementIndices.shift();
    }

    // Get unused suggestion
    let availableSuggestions = MOOD_RESPONSES.suggestions
        .map((_, i) => i)
        .filter(i => !usedSuggestionIndices.includes(i));

    if (availableSuggestions.length === 0) {
        usedSuggestionIndices = [];
        availableSuggestions = MOOD_RESPONSES.suggestions.map((_, i) => i);
    }

    const sugIndex = availableSuggestions[Math.floor(Math.random() * availableSuggestions.length)];
    usedSuggestionIndices.push(sugIndex);
    if (usedSuggestionIndices.length > MAX_HISTORY) {
        usedSuggestionIndices.shift();
    }

    // Select random emoji from all categories
    const categories = Object.keys(MOOD_RESPONSES.emojis);
    const randomCategory = categories[Math.floor(Math.random() * categories.length)];
    const emojis = MOOD_RESPONSES.emojis[randomCategory];
    const emoji = emojis[Math.floor(Math.random() * emojis.length)];

    return {
        emoji: emoji,
        encouragement: MOOD_RESPONSES.encouragements[encIndex],
        suggestion: MOOD_RESPONSES.suggestions[sugIndex]
    };
}

